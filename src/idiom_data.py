IDIOM_DICT = {}

# 构建索引
for idiom in COMMON_IDIOMS:
    first_char = idiom[0]
    if first_char not in IDIOM_DICT:
        IDIOM_DICT[first_char] = []
    IDIOM_DICT[first_char].append(idiom)


def get_idioms_by_first_char(char: str) -> list[str]:
    """根据首字获取成语列表"""
    return IDIOM_DICT.get(char, [])


def is_valid_idiom(idiom: str) -> bool:
    """检查是否为有效成语"""
    return idiom in COMMON_IDIOMS


def get_all_idioms() -> list[str]:
    """获取所有成语"""
    return COMMON_IDIOMS.copy()
